package com.topcoder.uml.stubclassgenerator;


/**
 * <p>
 * This exception encapsulates all configuration problems of component. It
 * extends from {@link CodeGenerationException}.
 * </p>
 *
 * @author tushak, TCSDEVELOPER
 * @version 1.0
 */
public class GeneratorConfigurationException extends CodeGenerationException {
    /**
     * <p>
     * Creates a new {@link GeneratorConfigurationException} with given text
     * message.
     * </p>
     *
     * @param message
     *            the message to be associated with this
     *            {@link GeneratorConfigurationException}
     */
    public GeneratorConfigurationException(String message) {
        super(message);
    }

    /**
     * <p>
     * Creates a new {@link GeneratorConfigurationException} with given text
     * message and underlying cause.
     * </p>
     *
     * @param message
     *            the message to be associated with this
     *            {@link GeneratorConfigurationException}
     * @param cause
     *            the underlying exception
     */
    public GeneratorConfigurationException(String message, Throwable cause) {
        super(message, cause);
    }
}
