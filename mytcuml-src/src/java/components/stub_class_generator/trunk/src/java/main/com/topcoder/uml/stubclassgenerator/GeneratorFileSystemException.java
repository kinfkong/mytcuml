package com.topcoder.uml.stubclassgenerator;


/**
 * <p>
 * This exception encapsulates all problems which may appear with file system
 * and input/output process. It extends from {@link CodeGenerationException}.>
 * </p>
 * <p>
 * Possible error situations:
 * <ul>
 * <li> Impossible to write a file (CodeWriter throws IOException) </li>
 * <li> Impossible to create or delete a directory (methods createDirectory()
 * and deleteDirectory() returns <code>false</code>)</li>
 * </ul>
 * </p>
 *
 * @author tushak, TCSDEVELOPER
 * @version 1.0
 */
public class GeneratorFileSystemException extends CodeGenerationException {
    /**
     * <p>
     * Creates a new {@link GeneratorFileSystemException} with given text
     * message.
     * </p>
     *
     * @param message
     *            the message to be associated with this
     *            {@link GeneratorFileSystemException}
     */
    public GeneratorFileSystemException(String message) {
        super(message);
    }

    /**
     * <p>
     * Creates a new {@link GeneratorFileSystemException} with given text
     * message and underlying cause.
     * </p>
     *
     * @param message
     *            the message to be associated with this
     *            {@link GeneratorFileSystemException}
     * @param cause
     *            the underlying exception
     */
    public GeneratorFileSystemException(String message, Throwable cause) {
        super(message, cause);
    }
}
