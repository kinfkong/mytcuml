package com.topcoder.gui.diagramviewer.edges;

import java.util.EventListener;

/**
 * <p>
 * This interface defines the contract that every way point event listener must follow.
 * </p>
 *
 * <p>
 * It contains the method to process the way point dragged event with the WayPointEvent given.
 * </p>
 *
 * <p>
 * Thread Safety : The implementations of this interface are not required to be thread safe.
 * It is expected to be used in a single thread.
 * </p>
 *
 * @author maone, TCSDEVELOPER
 * @version 1.0
 */
public interface WayPointListener extends EventListener {
    /**
     * <p>
     * Handle the way point dragged event.
     * </p>
     *
     * @param e a WayPointEvent instance used to indicate the way point drag event.
     */
    public void wayPointDragged(WayPointEvent e);
}
