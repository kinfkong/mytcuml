package com.topcoder.gui.diagramviewer.edges;

/**
 * <p>
 * This class is an enumeration and represents all the anchor types of text fields.
 * </p>
 *
 * <p>
 * It defines three values:
 * <ul>
 * <li>LeftEnding</li>
 * <li>RightEnding</li>
 * <li>Line</li>
 * </ul>
 * </p>
 *
 * <p>
 * Thread Safety : This class is an Enum and is thread safe.
 * </p>
 *
 * @author maone, TCSDEVELOPER
 * @version 1.0
 */
public enum AnchorType {
    /**
     * <p>
     * The LeftEnding anchor type.
     * </p>
     *
     * <p>
     * It means the text fields are anchored on the edge's left ending.
     * </p>
     */
    LeftEnding,

    /**
     * <p>
     * The RightEnding anchor type.
     * </p>
     *
     * <p>
     * It means the text fields are anchored on the edge's right ending.
     * </p>
     */
    RightEnding,

    /**
     * <p>
     * The Line anchor type.
     * </p>
     *
     * <p>
     * It means the text fields are anchored on the edge's line.
     * </p>
     */
    Line;
}
