package com.topcoder.xmi.writer.transformers.model.stresstests;

import java.util.Collection;

/**
 * This is a simple interface used to do unit test of the component.
 *
 * @author haozhangr
 * @version 1.0
 */
public interface StressInterface {
    /**
     * Get the namespace.
     *
     * @return the namespace.
     */
    public String getNamespace();
    /**
     * Get a boolean.
     *
     * @return a Boolean
     */
    public Boolean isLeaf();
    /**
     * Get a collection with name.
     *
     * @return a collection.
     */
    public Collection getChildren();
    /**
     * The method add child.
     *
     * @param child the String child
     */
    public void addChild(String child);
    /**
     * Get the excluded.
     *
     * @return a String
     */
    public String getExcluded();
    /**
     * Get the excluded.
     *
     * @return a String
     */
    public String getNotExcluded();
    /**
     * Get a instance that is a mock class.
     *
     * @return a InnerBean instance.
     */
    public StressUMLBean getStressUMLBean();
    /**
     * Get a instance of StressEnum.
     *
     * @return a StressEnum instance.
     */
    public StressEnum getStressEnum();
    /**
     * Get a instance of StressReference.
     *
     * @return a instance of StressReference
     */
    public StressReference getStressReference();
}
