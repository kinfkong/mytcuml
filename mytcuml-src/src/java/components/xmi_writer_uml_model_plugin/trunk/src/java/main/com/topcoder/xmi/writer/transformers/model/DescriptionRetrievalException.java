package com.topcoder.xmi.writer.transformers.model;

import com.topcoder.xmi.writer.XMITransformException;


/**
 * <p>
 * This exception is used to indicate any error occurs when retrieve xmi description.
 * </p>
 *
 * <p>
 * This exception can be thrown from {@link XMIDescription#getXMIElementDescription(Class)} method.
 * All implementation of the interface may throw this exception.
 * </p>
 *
 * This class is immutable, so it is thread-safe.
 *
 * @author maone, TCSDEVELOPER
 * @version 1.0
 */
public class DescriptionRetrievalException extends XMITransformException {
    /**
     * <p>Constructor with error message.</p>
     *
     * @param message the error message.
     */
    public DescriptionRetrievalException(String message) {
        super(message);
    }

    /**
     * <p>Constructor with the error message and inner cause.</p>
     *
     * @param message the error message.
     * @param cause the inner cause.
     */
    public DescriptionRetrievalException(String message, Throwable cause) {
        super(message, cause);
    }
}
