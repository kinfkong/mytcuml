package com.topcoder.gui.diagramviewer.elements.accuracytests;

import java.awt.Point;
import java.awt.event.MouseEvent;

import com.topcoder.gui.diagramviewer.elements.DragEventAdapter;

/**
 * A mocked class for abstract class <code>DragEventAdapter</code>.
 * @author Chenhong
 * @version 1.0
 *
 */
public class MyDragEventAdapter extends DragEventAdapter {

    /**
     * Implementation for mouse dragged.
     */
    public void mouseDragged(MouseEvent arg0) {
        // do nothing.
    }


    /**
     * Getter of lastPoint field.
     *
     * @return the lastPoint field value.
     */
    protected Point getLastPoint() {
        return super.getLastPoint();
    }

    /**
     * Setter of lastPoint field.
     * <p>
     * No restriction on lastPoint value.
     * </p>
     *
     * @param lastPoint the new lastPoint field value.
     */
    protected void setLastPoint(Point lastPoint) {
        super.setLastPoint(lastPoint);
    }
}
