package com.topcoder.gui.diagramviewer.elements.accuracytests;

import java.util.ArrayList;
import java.util.Collection;

import com.topcoder.gui.diagramviewer.elements.SelectionCornerType;

/**
 * A helper class for accuracy test.
 *
 * @author Chenhong
 * @version 1.0
 *
 */
public final class Helper {

    /**
     * <p>
     * Represent all the eight selection corner types.
     * </p>
     */
    public static Collection<SelectionCornerType> getCollection() {
        Collection<SelectionCornerType> collection = new ArrayList<SelectionCornerType>();
        collection.add(SelectionCornerType.EAST);
        collection.add(SelectionCornerType.NORTH);
        collection.add(SelectionCornerType.NORTHEAST);
        collection.add(SelectionCornerType.NORTHWEST);
        collection.add(SelectionCornerType.SOUTH);
        collection.add(SelectionCornerType.SOUTHEAST);
        collection.add(SelectionCornerType.SOUTHWEST);
        collection.add(SelectionCornerType.WEST);
        return collection;
    }
}
