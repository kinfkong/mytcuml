package com.topcoder.xmi.writer;

/**
 * <p>
 * This exception is thrown by the <code>{@link XMIWriter}</code> and <code>{@link XMITransformer}</code>
 * implementations if any error occurs during the transformation. It is mainly to wrap the underlying exceptions other
 * than the i/o related exceptions.
 * </p>
 *
 * @author Standlove, FireIce
 * @version 1.0
 */
public class XMITransformException extends XMIWriterException {

    /**
     * serial version unique id.
     */
    private static final long serialVersionUID = -4280850286923927667L;

    /**
     * <p>
     * Constructs an <code>{@link  XMITransformException}</code> with the error message.
     * </p>
     *
     * @param message
     *            the error message.
     */
    public XMITransformException(String message) {
        super(message);
    }

    /**
     * <p>
     * Constructs an <code>{@link  XMITransformException}</code> with the error message and the inner cause.
     * </p>
     *
     * @param message
     *            the error message.
     * @param cause
     *            the inner cause.
     */
    public XMITransformException(String message, Throwable cause) {
        super(message, cause);
    }
}
