package com.topcoder.umltool.xmiconverters.poseidon5;

import com.topcoder.xmi.reader.XMIHandler;
import com.topcoder.xmi.reader.XMIReader;

/**
 * <p><code>XMIConverterHandler</code> is package private thus can not be accessed from another
 * package, so this class extends it, and declared itself as public. In this way, it can be accessed
 * from any other class and <code>XMIConverterHandler</code> can be tested indirectly.
 * </p>
 *
 * @author abram
 * @version 1.0
 */
public class AccuracyXMIConverterHandlerWrapper extends XMIConverterHandler {

    /**
     * <p>
     * Simply delegate to the super constructor.
     * </p>
     *
     *
     * @param innerHandler
     *            the wrapped handler the converters will be added to
     * @param reader
     *            the XMIReader instance
     * @throws IllegalArgumentException
     *             if any argument is null
     */
    public AccuracyXMIConverterHandlerWrapper(XMIHandler innerHandler, XMIReader reader) {
        super(innerHandler, reader);
    }
}
