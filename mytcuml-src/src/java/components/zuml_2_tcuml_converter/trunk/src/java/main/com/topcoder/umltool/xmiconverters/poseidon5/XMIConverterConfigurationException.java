package com.topcoder.umltool.xmiconverters.poseidon5;

import com.topcoder.util.errorhandling.BaseException;


/**
 * <p>
 * This exception extends BaseException and is thrown if errors occurred when configuring
 * the XMIReader in XMIConvertersUtil or configuring any other classes in this component.
 * <p>
 *
 * <p>
 * Note: The based exception BaseException is from Base Exception 1.0 component becaues
 * XMI Reader 1.0 component uses it.
 * </p>
 *
 * <p>
 * <strong>Thread safety:</strong>
 * It is immutable and the based exception is thread-safe, so it is thread-safe.
 * </p>
 *
 * @author TCSDESIGNER, TCSDEVELOPER
 * @version 1.0
 */
public class XMIConverterConfigurationException extends BaseException {
    /**
     * <p>Serial version uid for the Serializable class.</p>
     */
    private static final long serialVersionUID = -3427096782208988901L;

    /**
     * <p>Create the exception with given error message.</p>
     *
     * @param message the error message
     */
    public XMIConverterConfigurationException(String message) {
        super(message);
    }

    /**
     * <p>Create the exception with given error message and the cause exception.</p>
     *
     * @param message the error message
     * @param cause the cause exception
     */
    public XMIConverterConfigurationException(String message, Throwable cause) {
        super(message, cause);
    }
}
