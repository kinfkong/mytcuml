package com.topcoder.gui.diagramviewer.uml.sequenceelements.edgeendings;

/**
 * <p>
 * The enum representing all possible kinds of the used properties for the
 * ConfiguredEdgeEnding.
 * </p>
 *
 * <p>
 * The enum elements corresponds to the ConfiguredEdgeEnding class instance
 * variables with the same names: strokeColor, fillColor and so on.
 * </p>
 *
 * <p>
 * Thread safety: The class is an enum and so is thread safe.
 * </p>
 *
 * @author MiG-29, TCSDEVELOPER
 * @version 1.1
 * @since 1.0
 */
public enum ConfiguredEdgeEndingPropertyType {
    /**
     * <p>Represents the STROKE_COLOR enum item.</p>
     *
     */
    STROKE_COLOR,

    /**
     * <p>Represents the FILL_COLOR enum item.</p>
     *
     */
    FILL_COLOR,

    /**
     * <p>Represents the ARROW_X_LENGTH enum item.</p>
     *
     */
    ARROW_X_LENGTH,

    /**
     * <p>Represents the ARROW_Y_LENGTH enum item.</p>
     *
     */
    ARROW_Y_LENGTH,

    /**
     * <p>Represents the RENDER_SCHEME enum item.</p>
     *
     */
    RENDER_SCHEME
}
