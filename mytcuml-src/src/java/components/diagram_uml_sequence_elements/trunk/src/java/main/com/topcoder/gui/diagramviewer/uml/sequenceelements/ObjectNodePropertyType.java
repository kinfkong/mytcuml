package com.topcoder.gui.diagramviewer.uml.sequenceelements;

/**
 * <p>
 * The enum representing all possible kinds of the used properties for the ObjectNode.
 * </p>
 *
 * <p>
 * The enum elements corresponds to the ObjectNode class instance variables with the
 * same names: strokeColor, fillColor and so on.
 * </p>
 *
 * <p>
 * Thread safety: The class is an enum and so is thread safe.
 * </p>
 *
 * @author MiG-29, TCSDEVELOPER
 * @version 1.1
 * @since 1.0
 */
public enum ObjectNodePropertyType {
    /**
     * <p>Represents the STROKE_COLOR enum item.</p>
     *
     */
    STROKE_COLOR,

    /**
     * <p>Represents the FILL_COLOR enum item.</p>
     *
     */
    FILL_COLOR,

    /**
     * <p>Represents the FILL_COLOR2 enum item.</p>
     *
     */
    FILL_COLOR2,

    /**
     * <p>Represents the SHADOW_COLOR enum item.</p>
     *
     */
    SHADOW_COLOR,

    /**
     * <p>Represents the FONT_COLOR enum item.</p>
     *
     */
    FONT_COLOR ,

    /**
     * <p>Represents the FONT_FAMILY enum item.</p>
     *
     */
    FONT_FAMILY,

    /**
     * <p>Represents the FONT_STYLE enum item.</p>
     *
     */
    FONT_STYLE,

    /**
     * <p>Represents the FONT_SIZE enum item.</p>
     *
     */
    FONT_SIZE,

    /**
     * <p>Represents the WIDTH enum item.</p>
     *
     */
    WIDTH,

    /**
     * <p>Represents the HEIGHT enum item.</p>
     *
     */
    HEIGHT,

    /**
     * <p>Represents the ROUNDING_RADIUS enum item.</p>
     *
     */
    ROUNDING_RADIUS,

    /**
     * <p>Represents the SHADOW_LENGTH enum item.</p>
     *
     */
    SHADOW_LENGTH,

    /**
     * <p>Represents the MINIMUM_WIDTH enum item.</p>
     *
     */
    MINIMUM_WIDTH,

    /**
     * <p>Represents the MINIMUM_HEIGHT enum item.</p>
     *
     */
    MINIMUM_HEIGHT,

    /**
     * <p>Represents the RENDER_SCHEME enum item.</p>
     *
     */
    RENDER_SCHEME;
}
