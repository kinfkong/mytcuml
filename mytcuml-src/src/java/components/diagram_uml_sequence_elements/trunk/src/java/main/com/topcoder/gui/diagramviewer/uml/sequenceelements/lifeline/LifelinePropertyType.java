package com.topcoder.gui.diagramviewer.uml.sequenceelements.lifeline;

/**
 * <p>
 * The enum representing all possible kinds of the used properties for the Lifeline.
 * </p>
 *
 * <p>
 * The enum elements corresponds to the Lifeline class instance variables with the same names:
 * strokeColor, shadowColor and so on.
 * </p>
 *
 * Thread safety:The enums are thread safe.
 *
 * @author gniuxiao, TCSDEVELOPER
 * @version 1.1
 * @since 1.1
 */
public enum LifelinePropertyType {
    /**
     * <p>
     * Represents the STROKE_COLOR enum item.
     * </p>
     *
     */
    STROKE_COLOR,
    /**
     * <p>
     * Represents the SHADOW_COLOR enum item.
     * </p>
     *
     */
    SHADOW_COLOR,
    /**
     * <p>
     * Represents the DASH_LENGTH enum item.
     * </p>
     *
     */
    DASH_LENGTH,
    /**
     * <p>
     * Represents the BLANK_LENGTH enum item.
     * </p>
     *
     */
    BLANK_LENGTH,
    /**
     * <p>
     * Represents the RENDER_SCHEME enum item.
     * </p>
     *
     */
    RENDER_SCHEME,
    /**
     * <p>
     * Represents the LIFELINE_LENGTH enum item.
     * </p>
     *
     */
    LIFELINE_LENGTH,
    /**
     * <p>
     * Represents the SHADOW_LENGTH enum item.
     * </p>
     *
     */
    SHADOW_LENGTH;
}
