package com.topcoder.gui.diagramviewer.uml.sequenceelements;

/**
 * <p>
 * The enum representing all possible kinds of the used properties for the SequenceEdge.
 * </p>
 *
 * <p>
 * The enum elements corresponds to the SequenceEdge class instance variables with the
 * same names: strokeColor, fillColor and so on.
 * </p>
 *
 * <p>
 * Thread safety: The class is an enum and so is thread safe.
 * </p>
 *
 * @author MiG-29, TCSDEVELOPER
 * @version 1.1
 * @since 1.0
 */
public enum SequenceEdgePropertyType {
    /**
     * <p>Represents the STROKE_COLOR enum item.</p>
     *
     */
    STROKE_COLOR,

    /**
     * <p>Represents the FILL_COLOR enum item.</p>
     *
     */
    FILL_COLOR,

    /**
     * <p>Represents the FILL_COLOR2 enum item.</p>
     *
     */
    FILL_COLOR2,

    /**
     * <p>Represents the SHADOW_COLOR enum item.</p>
     *
     */
    SHADOW_COLOR,

    /**
     * <p>Represents the FONT_COLOR enum item.</p>
     *
     */
    FONT_COLOR,

    /**
     * <p>Represents the TEXT_BOX_STROKE_COLOR enum item.</p>
     *
     */
    TEXT_BOX_STROKE_COLOR,

    /**
     * <p>Represents the TEXT_BOX_FILL_COLOR enum item.</p>
     *
     */
    TEXT_BOX_FILL_COLOR,

    /**
     * <p>Represents the TEXT_BOX_FILL_COLOR2 enum item.</p>
     *
     */
    TEXT_BOX_FILL_COLOR2,

    /**
     * <p>Represents the FONT_FAMILY enum item.</p>
     *
     */
    FONT_FAMILY,

    /**
     * <p>Represents the FONT_STYLE enum item.</p>
     *
     */
    FONT_STYLE,

    /**
     * <p>Represents the FONT_SIZE enum item.</p>
     *
     */
    FONT_SIZE,

    /**
     * <p>Represents the DASH_LENGTH enum item.</p>
     *
     */
    DASH_LENGTH,

    /**
     * <p>Represents the BLANK_LENGTH enum item.</p>
     *
     */
    BLANK_LENGTH,

    /**
     * <p>Represents the RENDER_SCHEME enum item.</p>
     *
     */
    RENDER_SCHEME,
}
