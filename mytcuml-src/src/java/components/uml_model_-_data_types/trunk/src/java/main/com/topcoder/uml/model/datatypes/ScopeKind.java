package com.topcoder.uml.model.datatypes;

/**
 * <p>
 * In the metamodel, ScopeKind defines an enumeration that denotes whether a feature belongs to
 * individual instances or an entire classifier.
 * </p>
 *
 * @author argolite, Achilles_2005
 * @version 1.0
 */
public enum ScopeKind {

    /**
     * <p>
     * The feature pertains to Instances of a Classifier. For example, it is a distinct Attribute in
     * each Instance or an Operation that works on an Instance.
     * </p>
     *
     */
    INSTANCE,

    /**
     * <p>
     * The feature pertains to an entire Classifier. For example, it is an Attribute shared by the
     * entire Classifier or an Operation that works on the Classifier, such as a creation operation.
     * </p>
     *
     */
    CLASSIFIER
}
