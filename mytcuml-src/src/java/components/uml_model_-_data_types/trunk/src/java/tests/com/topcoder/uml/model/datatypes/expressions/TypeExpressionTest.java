package com.topcoder.uml.model.datatypes.expressions;

import junit.framework.TestCase;

/**
 * <p>
 * Unit test for <code>TypeExpression</code>.
 * </p>
 *
 * @author Achilles_2005
 * @version 1.0
 */
public class TypeExpressionTest extends TestCase {

    /**
     * <p>
     * Tests accuracy of <code>TypeExpression</code>.
     * </p>
     * <p>
     * <code>TypeExpression</code> should inherit from <code>Expression</code>.
     * </p>
     */
    public void testTypeExpressionAccuracyInheritExpression() {
        assertTrue("TypeExpression should inherit from Expression", TypeExpression.class
            .getInterfaces()[0].equals(Expression.class));
    }

}
