import junit.framework.TestCase;

/**
 * <p>
 * Unit test for <code>ProcedureExpressionImpl</code>.
 * </p>
 *
 * @author Achilles_2005
 * @version 1.0
 */
public class ProcedureExpressionImplTest extends TestCase {

    /**
     * <p>
     * Tests accuracy of <code>ProcedureExpressionImpl</code>.
     * </p>
     * <p>
     * <code>ProcedureExpressionImpl</code> should inherit from <code>ExpressionImpl</code>.
     * </p>
     */
    public void testProcedureExpressionImplAccuracyInheritExpressionImpl() {
        assertTrue("ProcedureExpressionImpl should inherit from ExpressionImpl",
            ProcedureExpressionImpl.class.getSuperclass().equals(ExpressionImpl.class));
    }

    /**
     * <p>
     * Tests accuracy of <code>ProcedureExpressionImpl</code>.
     * </p>
     * <p>
     * <code>ProcedureExpressionImpl</code> should inherit from <code>ProcedureExpression</code>.
     * </p>
     */
    public void testProcedureExpressionImplAccuracyInheritProcedureExpression() {
        assertTrue("ProcedureExpressionImpl should inherit from ProcedureExpression",
            ProcedureExpressionImpl.class.getInterfaces()[0].equals(ProcedureExpression.class));
    }

    /**
     * <p>
     * Tests accuracy of <code>ProcedureExpressionImpl</code>.
     * </p>
     * <p>
     * Object created should not be null.
     * </p>
     */
    public void testProcedureExpressionImplAccuracy() {
        assertNotNull("Object created should not be null.", new ProcedureExpressionImpl());
    }

}
