package com.topcoder.uml.model.datatypes.expressions;

import junit.framework.TestCase;

/**
 * <p>
 * Unit test for <code>BooleanExpression</code>.
 * </p>
 *
 * @author Achilles_2005
 * @version 1.0
 */
public class BooleanExpressionTest extends TestCase {

    /**
     * <p>
     * Tests accuracy of <code>BooleanExpression</code>.
     * </p>
     * <p>
     * <code>BooleanExpression</code> should inherit from <code>Expression</code>.
     * </p>
     */
    public void testBooleanExpressionAccuracyInheritExpression() {
        assertTrue("BooleanExpression should inherit from Expression", BooleanExpression.class
            .getInterfaces()[0].equals(Expression.class));
    }

}
