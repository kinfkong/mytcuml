package com.topcoder.uml.model.datatypes.expressions;

import junit.framework.TestCase;

/**
 * <p>
 * Unit test for <code>MappingExpressionImpl</code>.
 * </p>
 *
 * @author Achilles_2005
 * @version 1.0
 */
public class MappingExpressionImplTest extends TestCase {

    /**
     * <p>
     * Tests accuracy of <code>MappingExpressionImpl</code>.
     * </p>
     * <p>
     * <code>MappingExpressionImpl</code> should inherit from <code>ExpressionImpl</code>.
     * </p>
     */
    public void testMappingExpressionImplAccuracyInheritExpressionImpl() {
        assertTrue("MappingExpressionImpl should inherit from ExpressionImpl",
            MappingExpressionImpl.class.getSuperclass().equals(ExpressionImpl.class));
    }

    /**
     * <p>
     * Tests accuracy of <code>MappingExpressionImpl</code>.
     * </p>
     * <p>
     * <code>MappingExpressionImpl</code> should inherit from <code>MappingExpression</code>.
     * </p>
     */
    public void testMappingExpressionImplAccuracyInheritMappingExpression() {
        assertTrue("MappingExpressionImpl should inherit from MappingExpression",
            MappingExpressionImpl.class.getInterfaces()[0].equals(MappingExpression.class));
    }

    /**
     * <p>
     * Tests accuracy of <code>MappingExpressionImpl</code>.
     * </p>
     * <p>
     * Object created should not be null.
     * </p>
     */
    public void testMappingExpressionImplAccuracy() {
        assertNotNull("Object created should not be null.", new MappingExpressionImpl());
    }

}
