package com.topcoder.swing.imageexporter;

/**
 * <p>This exception is thrown if any error occurs when reading from config file: for example the config file is
 * malformed.</p>
 * <p>Thread-Safety: This class is immutable and thread safe.</p>
 *
 * @author fabrizyo, TCSDEVELOPER
 * @version 1.0
 */
public class ImageExporterConfigurationException extends ImageExporterException {
    /**
     * <p>Creates a new <code>ImageExporterConfigurationException</code> with given error message.</p>
     * @param message the error message of this exception
     */
    public ImageExporterConfigurationException(String message) {
        super(message);
    }

    /**
     * <p>Creates a new <code>ImageExporterConfigurationException</code> with given error message and inner cause.</p>
     * @param message the error message of this exception
     * @param cause the inner cause of this exception
     */
    public ImageExporterConfigurationException(String message, Throwable cause) {
        super(message, cause);
    }
}
