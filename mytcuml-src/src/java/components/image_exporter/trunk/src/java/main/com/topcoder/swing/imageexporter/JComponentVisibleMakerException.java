package com.topcoder.swing.imageexporter;

/**
 * <p>This exception is thrown by JComponentVisibleMaker if any error occurs during the visibility making process.</p>
 * <p>Thread-Safety: This class is thread safe as it has no state and its super class is also thread safe.</p>
 *
 * @author fabrizyo, TCSDEVELOPER
 * @version 1.0
 */
public class JComponentVisibleMakerException extends ImageExporterException {
    /**
     * <p>Creates a new <code>JComponentVisibleMakerException</code> with given error message and inner cause.</p>
     * @param message the error message of this exception
     */
    public JComponentVisibleMakerException(String message) {
        super(message);
    }

    /**
     * <p>Creates a new <code>JComponentVisibleMakerException</code> with given error message and inner cause.</p>
     * @param message the error message of this exception
     * @param cause the inner cause of this exception
     */
    public JComponentVisibleMakerException(String message, Throwable cause) {
        super(message, cause);
    }
}
