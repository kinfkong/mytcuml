package com.topcoder.uml.actions.diagram.elements.compartmentextraction;

import com.topcoder.util.actionmanager.ActionExecutionException;

/**
 * <p>
 * Represent an Exception corresponds to a situation where the compartment that is supposed
 * to be there, is not there.
 * </p>
 *
 * <p>
 * This exception might be thrown in implementation of CompartmentExtractor interface
 * when the intended compartment is not available in the element.
 * </p>
 *
 * <p>
 * Thread Safety : This class is immutable and so is thread safe.
 * </p>
 *
 * @author bramandia, TCSDEVELOPER
 * @version 1.0
 */
public class CompartmentNotFoundException extends ActionExecutionException {
    /**
     * <p>
     * Create a new CompartmentNotFoundException with given message.
     * </p>
     *
     * @param msg exception message
     */
    public CompartmentNotFoundException(String msg) {
        super(msg);
    }

    /**
     * <p>
     * Create a new CompartmentNotFoundException with given message and cause.
     * </p>
     *
     * @param msg exception message
     * @param cause exception cause
     */
    public CompartmentNotFoundException(String msg, Throwable cause) {
        super(msg, cause);
    }
}
