package com.topcoder.uml.actions.diagram.elements.compartmentextraction;

import com.topcoder.util.actionmanager.ActionExecutionException;

/**
 * <p>
 * Represent an Exception to represent a situation where the intended compartment is
 * not supported or not supposed to be available.
 * </p>
 *
 * <p>
 * This exception might be thrown in implementation of CompartmentExtractor interface
 * where the intended compartment is not supported by the element.
 * </p>
 *
 * <p>
 * Thread Safety : This class is immutable and so is thread safe.
 * </p>
 *
 * @author bramandia, TCSDEVELOPER
 * @version 1.0
 */
public class CompartmentNotSupportedException extends ActionExecutionException {
    /**
     * <p>
     * Create a new CompartmentNotSupportedException with given message.
     * </p>
     *
     * @param msg exception message
     */
    public CompartmentNotSupportedException(String msg) {
        super(msg);
    }

    /**
     * <p>
     * Create a new CompartmentNotSupportedException with given message and cause.
     * </p>
     *
     * @param msg exception message
     * @param cause exception cause
     */
    public CompartmentNotSupportedException(String msg, Throwable cause) {
        super(msg, cause);
    }
}
