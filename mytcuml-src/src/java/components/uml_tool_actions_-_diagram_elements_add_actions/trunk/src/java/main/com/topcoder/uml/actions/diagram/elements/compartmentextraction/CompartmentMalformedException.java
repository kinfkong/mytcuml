package com.topcoder.uml.actions.diagram.elements.compartmentextraction;

import com.topcoder.util.actionmanager.ActionExecutionException;

/**
 * <p>
 * Represent an Exception to represent a situation where the element being processed is malformed,
 * it does not follow the proper compartment hierarchy.
 * </p>
 *
 * <p>
 * This exception might be thrown in implementation of CompartmentExtractor interface when
 * add/update compartment with incorrect semantic Model.
 * </p>
 *
 * @author bramandia, TCSDEVELOPER
 * @version 1.0
 */
public class CompartmentMalformedException extends ActionExecutionException {
    /**
     * <p>
     * Create a new CompartmentMalformedException with given message.
     * </p>
     *
     * @param msg the exception message
     */
    public CompartmentMalformedException(String msg) {
        super(msg);
    }

    /**
     * <p>
     * Create a new CompartmentMalformedException with given message and cause.
     * </p>
     *
     * @param msg exception message
     * @param cause exception cause
     */
    public CompartmentMalformedException(String msg, Throwable cause) {
        super(msg, cause);
    }
}
