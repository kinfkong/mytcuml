package com.topcoder.swing.print;

import com.topcoder.util.errorhandling.BaseException;


/**
 * <p>The <code>ConfigurationException</code> exception is used to wrap any specific exception or indicate any
 * error when the config is invalid. This exception is thrown in the <code>PrintManager</code>.</p>
 *  <p>This class is thread safe since it is immutable.</p>
 *
 * @author Xuchen, TCSDEVELOPER
 * @version 1.0
 */
public class ConfigurationException extends BaseException {
    /**
     * <p>Creates a new <code>ConfigurationException</code> with given error message.</p>
     * @param message the error message of this exception
     */
    public ConfigurationException(String message) {
        super(message);
    }

    /**
     * <p>Creates a new <code>ConfigurationException</code> with given error message and inner cause.</p>
     * @param message the error message of this exception
     * @param cause the inner cause of this exception
     */
    public ConfigurationException(String message, Throwable cause) {
        super(message, cause);
    }
}
