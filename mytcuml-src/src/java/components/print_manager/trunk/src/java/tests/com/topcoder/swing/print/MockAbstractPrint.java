package com.topcoder.swing.print;

import java.awt.Component;
import java.awt.Graphics;
import java.awt.Rectangle;
import java.awt.print.PageFormat;


/**
 * Mock implement of {@link AbstractPrint} for test purpose.
 *
 * @author TCSDEVELOPER
 * @version 1.0
 */
public class MockAbstractPrint extends AbstractPrint {
/**
     * Creates a new MockAbstractPrint object.
     *
     * @param component the component to print
     */
    public MockAbstractPrint(Component component) {
        super(component);
    }

/**
     * Creates a new MockAbstractPrint object.
     *
     * @param component the component to print
     * @param rectangle the rectangle area within which the component to print
     */
    public MockAbstractPrint(Component component, Rectangle rectangle) {
        super(component, rectangle);
    }

    /**
     * Does nothing.
     *
     * @param graphics ignore
     * @param pageFormat ignore
     * @param pageIndex ignore
     *
     * @return ignore
     */
    public int print(Graphics graphics, PageFormat pageFormat, int pageIndex) {
        return 0;
    }
}
