package com.topcoder.swing.print;

import com.topcoder.util.errorhandling.BaseException;


/**
 * <p>The <code>PrintFormatRetrieveException</code> exception is used to wrap any specific exception or indicate
 * any error when retrieving user's preference of Print Format or Scaling type. This exception is thrown by the
 * <code>PrintFormatRetriever</code> implementations and PrintManager which use PrintFormatRetriever
 * implementations.</p>
 *  <p>This class is thread safe since it is immutable.</p>
 *
 * @author Xuchen, TCSDEVELOPER
 * @version 1.0
 */
public class PrintFormatRetrieveException extends BaseException {
    /**
     * <p>Creates a new <code>PrintFormatRetrieveException</code> with given error message.</p>
     *
     * @param message the error message of this exception
     */
    public PrintFormatRetrieveException(String message) {
        super(message);
    }

    /**
     * <p>Creates a new <code>PrintFormatRetrieveException</code> with given error message and inner cause.</p>
     *
     * @param message the error message of this exception
     * @param cause the inner cause of this exception
     */
    public PrintFormatRetrieveException(String message, Throwable cause) {
        super(message, cause);
    }
}
