package com.topcoder.swing.print;

import com.topcoder.util.errorhandling.BaseRuntimeException;


/**
 * <p>The <code>PrintingException</code> exception is used to wrap any specific exception or indicate any error
 * when printing given java.awt.Component. This exception are thrown by the <code>PrintManager</code> class.</p>
 *  <p>This class is thread safe since it is immutable.</p>
 *
 * @author Xuchen, TCSDEVELOPER
 * @version 1.0
 */
public class PrintingException extends BaseRuntimeException {
    /**
     * <p>Creates a new <code>PrintingException</code> with given error message.</p>
     *
     * @param message the error message of this exception
     */
    public PrintingException(String message) {
        super(message);
    }

    /**
     * <p>Creates a new <code>PrintingException</code> with given error message and inner cause.</p>
     *
     * @param message the error message of this exception
     * @param cause the inner cause of this exception
     */
    public PrintingException(String message, Throwable cause) {
        super(message, cause);
    }
}
