package com.topcoder.gui.panels.properties;

import com.topcoder.util.errorhandling.BaseException;

/**
 * <p>
 * This class provides an exception to be thrown when there is any errors during retrieval
 * of configuration.
 * </p>
 *
 * <p>
 * This may include missing required property values or invalid values.
 * </p>
 *
 * <p>
 * Thread-safety: This class is thread-safe as its superclass is thread-safe.
 * </p>
 *
 * @author ch_music, TCSDEVELOPER
 * @version 1.0
 */
public class PropertiesPanelConfigurationException extends BaseException {
    /**
     * <p>
     * Creates a new <code>PropertiesPanelConfigurationException</code> with the given error message.
     * </p>
     *
     * @param message the error message
     */
    public PropertiesPanelConfigurationException(String message) {
        super(message);
    }

    /**
     * <p>
     * Creates a new <code>PropertiesPanelConfigurationException</code> with the given error message and cause.
     * </p>
     *
     * @param message the error message
     * @param cause the cause of the exception
     */
    public PropertiesPanelConfigurationException(String message, Throwable cause) {
        super(message, cause);
    }
}