package com.topcoder.gui.panels.properties;

/**
 * <p>
 * This enumeration contains the possible operation on a property.
 * </p>
 *
 * <p>
 * The possible operations are <code>ADD</code>, <code>REMOVE</code> and <code>MODIFY</code>.
 * </p>
 *
 * <p>
 * Thread-safety: Enum is thread-safe.
 * </p>
 *
 * @author ch_music, TCSDEVELOPER
 * @version 1.0
 */
public enum PropertyOperation {
    /**
     * <p>
     * Represents an add operation for a property.
     * </p>
     */
    ADD,

    /**
     * <p>
     * Represents a remove operation for a property.
     * </p>
     */
    REMOVE,

    /**
     * <p>
     * Represents a modify operation for a property.
     * </p>
     */
    MODIFY;
}