package com.topcoder.uml.actions.diagram.elements;

import com.topcoder.util.actionmanager.ActionExecutionException;

/**
 * <p>
 * This class extends the <code>ActionExecutionException</code> class,
 * and this exception is used if the clone operation fails.
 * </p>
 * @version 1.0
 * @author TCSDEVELOPER
 */


@SuppressWarnings("serial")
public class ElementCloneException extends ActionExecutionException {
    /**
     * <p>
     * Create a new <code>ElementCloneException</code> with error message.
     * </p>
     * @param message the error message.
     */
    public ElementCloneException(String message) {
        super(message);
    }

    /**
     * <p>
     * Create a new <code>ElementCloneException</code> with error message
     * and the cause exception.
     * </p>
     * @param message the error message
     * @param cause the inner cause.
     */
    public ElementCloneException(String message, Throwable cause) {
        super(message, cause);
    }
}
