package com.topcoder.gui.toolbars.elements;

import javax.swing.JToggleButton;

/**
 * <p>
 * This interface acts as an interested listener to the specified selected elements. When a specified element is
 * selected, the listener will receive the notification, this is implemented by calling
 * <code>elementSelected</code> method.
 * </p>
 * <p>
 * Thread safety: This interface can be implemented in a thread safe manner or otherwise.
 * </p>
 *
 * @author wang_liang, enefem21
 * @version 1.0
 */
public interface ElementSelectedListener {

    /**
     * <p>
     * The method to be called when a specified element is selected. It will notify which button is selected and
     * which section panel the button is added into.
     * </p>
     *
     * @param button
     *            the JToggleButton instance that is registered by this listener
     * @param sectionPanel
     *            the SectionPanel instance that the button is added into.
     */
    public void elementSelected(JToggleButton button, SectionPanel sectionPanel);

}
