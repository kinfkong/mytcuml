package com.topcoder.uml.importer.jarimporter;

import com.topcoder.util.errorhandling.BaseCriticalException;
import com.topcoder.util.errorhandling.ExceptionData;

/**
 * <p>
 * This is a custom exception if any errors occurred when importing the jar to the TC UML model and
 * diagram.
 * </p>
 * <p>
 * It extends from <code>BaseCriticalException</code>.
 * </p>
 *
 * @author dmks, TCSDEVELOPER
 * @version 1.0
 */
public class JarImporterException extends BaseCriticalException {
    /**
     * Creates a new instance with the given message.
     *
     * @param message Explanation of the error. Can be empty string or null (useless, but allowed).
     */
    public JarImporterException(String message) {
        super(message);
    }

    /**
     * Creates a new instance with the given message and cause.
     *
     * @param message Explanation of the error. Can be empty string or null (useless, but allowed).
     * @param cause Underlying cause of the error. Can be null, which means that initial exception
     * is nonexistent or unknown.
     */
    public JarImporterException(String message, Throwable cause) {
        super(message, cause);
    }

    /**
     * Creates a new instance with the given message and exception data.
     *
     * @param data The additional data to attach to the exception - if this is null, a new
     * <code>ExceptionData</code> will be automatically used instead.
     * @param message Explanation of the error. Can be empty string or null (useless, but allowed).
     */
    public JarImporterException(String message, ExceptionData data) {
        super(message, data);
    }

    /**
     * Creates a new instance with the given message,cause and exception data.
     *
     * @param data The additional data to attach to the exception - if this is null, a new
     * <code>ExceptionData</code> will be automatically used instead.
     * @param message Explanation of the error. Can be empty string or null (useless, but allowed).
     * @param cause Underlying cause of the error. Can be null, which means that initial exception
     * is nonexistent or unknown.
     */
    public JarImporterException(String message, Throwable cause, ExceptionData data) {
        super(message, cause, data);
    }
}
