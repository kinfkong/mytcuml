package com.topcoder.util.eventmanager;

/**
 * This enumeration represents the validation result type.
 *
 * <p>
 * <b>Thread Safety:</b>
 * </p>
 *
 * <p>
 * This enumeration is thread-safe by being immutable.
 * </p>
 *
 * @author kinzz, TCSDEVELOPER
 * @version 1.0
 *
 */
public enum EventValidation {
    /**
     * <p>
     * Represents the validation succeeded with no change.
     * </p>
    */
    SUCCESSFUL,
   /**
    * <p>
    * Represents the validation succeeded, but the action was modified.
    * </p>
    */
    EVENT_MODIFIED,
   /**
    * <p>
    * Represents the action is not valid.
    * </p>
    */
    DENIED;
}
