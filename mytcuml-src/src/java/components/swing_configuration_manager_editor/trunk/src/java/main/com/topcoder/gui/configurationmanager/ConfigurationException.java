package com.topcoder.gui.configurationmanager;

import com.topcoder.util.errorhandling.ExceptionData;

/**
 * This exception is thrown if the configuration is bad: missing required
 * properties or in the bad format.
 *
 * @author TCSDESIGNER, TCSDEVELOPER
 * @version 1.0
 */
public class ConfigurationException extends ConfigurationManagerEditorPanelException {
    /**
     * Constructor with error message.
     *
     * @param message
     *            the error message
     */
    public ConfigurationException(String message) {
        super(message);
    }

    /**
     * Constructor with error message and inner cause.
     *
     * @param message
     *            the error message
     * @param cause
     *            the cause of this exception
     */
    public ConfigurationException(String message, Throwable cause) {
        super(message, cause);
    }

    /**
     * Constructor with error message and exception data.
     *
     * @param data
     *            the exception data
     * @param message
     *            the error message
     */
    public ConfigurationException(String message, ExceptionData data) {
        super(message, data);
    }

    /**
     * Constructor with error message and inner cause and exception data.
     *
     * @param data
     *            the exception data
     * @param message
     *            the error message
     * @param cause
     *            the cause of this exception
     */
    public ConfigurationException(String message, Throwable cause, ExceptionData data) {
        super(message, cause, data);
    }
}
