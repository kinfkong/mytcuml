package com.topcoder.macosx;

import com.topcoder.util.errorhandling.BaseException;

/**
 * <p>
 * The <code>MacOSXEventHandlerException</code> exception is used to wrap any specific exception or indicate any errors
 * that can occur when there is a problem with Mac OS X specific EAWT library. These exceptions are thrown by the
 * <code>MacOSXAdapter</code>.
 * </p>
 * <p>
 * This class is thread safe since it is immutable.
 * </p>
 * @author L0s7man 
 * @version 1.0
 */
public class MacOSXEventHandlerException extends BaseException {

    /**
     * <p>
     * Creates a new <code>MacOSXEventHandlerException</code> with given error message.
     * </p>
     * @param message
     *            the error message of this exception
     */
    public MacOSXEventHandlerException(String message) {
        super(message);
    }

    /**
     * <p>
     * Creates a new <code>MacOSXEventHandlerException</code> with given error message and inner cause.
     * </p>
     * @param message
     *            the error message of this exception
     * @param cause
     *            the inner cause of this exception
     */
    public MacOSXEventHandlerException(String message, Throwable cause) {
        super(message, cause);
    }
}
