package com.topcoder.gui.closabletabbedpane;

import com.topcoder.util.errorhandling.BaseNonCriticalException;
import com.topcoder.util.errorhandling.ExceptionData;

/**
 * <p>
 * This exception indicates an error during configuration of the ClosableTabbedPane. It is thrown by the constructor
 * which takes the namespace as an argument.
 * </p>
 * <p>
 * Thread Safety: This class is not thread safe as its super class is not.
 * </p>
 * @author humblefool, TCSDEVELOPER
 * @version 1.0
 */
public class ClosableTabbedPaneConfigurationException extends BaseNonCriticalException {
    /**
     * The serial version UID.
     */
    private static final long serialVersionUID = 7350786975489838258L;

    /**
     * Constructs this exception without error message, cause or exception data.
     */
    public ClosableTabbedPaneConfigurationException() {
        super();
    }

    /**
     * Constructs this exception with an error message.
     * @param message
     *            the exception message
     */
    public ClosableTabbedPaneConfigurationException(String message) {
        super(message);
    }

    /**
     * Constructs this exception with an error message and a cause.
     * @param message
     *            the error message describing this exception.
     * @param cause
     *            the cause of this exception, possibly null.
     */
    public ClosableTabbedPaneConfigurationException(String message, Throwable cause) {
        super(message, cause);
    }

    /**
     * Constructs this exception with an error message and a cause and exception data.
     * @param message
     *            the error message describing this exception.
     * @param cause
     *            the cause of this exception, possibly null.
     * @param data
     *            the exception data, possibly null
     */
    public ClosableTabbedPaneConfigurationException(String message, Throwable cause, ExceptionData data) {
        super(message, cause, data);
    }
}
