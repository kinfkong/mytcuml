package com.topcoder.diagraminterchange;

import java.io.Serializable;

/**
 * <p>
 * Properties specify the appearance of DiagramElements. There are a set of standard properties but
 * non-standard ones could be added too.
 * </p>
 * <p>
 * <b>Thread Safety:</b> This class is mutable and is not thread-safe, since thread safety is not required.
 * Instead, the calling application is required to ensure thread safety.
 * </p>
 *
 * @author caru, TCSDEVELOPER
 * @version 1.0
 */
public class Property implements Serializable {

    /**
     * <p>
     * Represents the key of the Property. Can be null. Can be empty. Default to null.
     * </p>
     */
    private String key;

    /**
     * <p>
     * Represents the value of the Property. Can be null. Can be empty. Default to null.
     * </p>
     */
    private String value;

    /**
     * <p>
     * Creates an instance of Property.
     * </p>
     */
    public Property() {
        // empty constructor
    }

    /**
     * <p>
     * Gets the key of this Property.
     * </p>
     *
     * @return the key of this Property
     */
    public String getKey() {
        return key;
    }

    /**
     * <p>
     * Sets the key of this Property. Can be null. Can be empty.
     * </p>
     *
     * @param key the new key for this Property
     */
    public void setKey(String key) {
        this.key = key;
    }

    /**
     * <p>
     * Gets the value of this Property.
     * </p>
     *
     * @return the value of this Property
     */
    public String getValue() {
        return value;
    }

    /**
     * <p>
     * Sets the value of this Property. Can be null. Can be empty.
     * </p>
     *
     * @param value the new value for this Property
     */
    public void setValue(String value) {
        this.value = value;
    }
}