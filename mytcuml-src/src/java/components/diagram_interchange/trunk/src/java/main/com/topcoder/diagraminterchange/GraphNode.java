package com.topcoder.diagraminterchange;

/**
 * <p>
 * This class and GraphEdge are the core classes of Diagram Interchange and every visible model element is
 * represented by them. The base class of GraphNode and GraphEdge is GraphElement. Examples of GraphNodes in
 * class diagram can be a class, an attributes compartment, an attribute.
 * </p>
 * <p>
 * <b>Thread Safety:</b> This class is mutable and is not thread-safe, since thread safety is not required.
 * Instead, the calling application is required to ensure thread safety.
 * </p>
 *
 * @author caru, TCSDEVELOPER
 * @version 1.0
 */
public class GraphNode extends GraphElement {

    /**
     * <p>
     * Represents the size of this GraphNode. Can be null. Default to null.
     * </p>
     */
    private Dimension size;

    /**
     * <p>
     * Creates an instance of GraphNode.
     * </p>
     */
    public GraphNode() {
        super();
    }

    /**
     * <p>
     * Gets the size of this GraphNode.
     * </p>
     *
     * @return the size of this GraphNode
     */
    public Dimension getSize() {
        return size;
    }

    /**
     * <p>
     * Sets the size of this GraphNode. Can be null.
     * </p>
     *
     * @param size the new size of this GraphNode
     */
    public void setSize(Dimension size) {
        this.size = size;
    }
}