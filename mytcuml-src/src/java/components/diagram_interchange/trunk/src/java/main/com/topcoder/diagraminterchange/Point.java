package com.topcoder.diagraminterchange;

import java.io.Serializable;

/**
 * <p>
 * This is the basic implementation of a Point with x and y coordinates.
 * </p>
 * <p>
 * <b>Thread Safety:</b> This class is mutable and is not thread-safe, since thread safety is not required.
 * Instead, the calling application is required to ensure thread safety.
 * </p>
 *
 * @author caru, TCSDEVELOPER
 * @version 1.0
 */
public class Point implements Serializable {

    /**
     * <p>
     * Represents the x coordinate of this Point. Default to 0.0.
     * </p>
     */
    private double x;

    /**
     * <p>
     * Represents the y coordinate of this Point. Default to 0.0.
     * </p>
     */
    private double y;

    /**
     * <p>
     * Creates an instance of Point.
     * </p>
     */
    public Point() {
        // empty constructor
    }

    /**
     * <p>
     * Gets the x coordinate of this Point.
     * </p>
     *
     * @return the x coordinate of this Point
     */
    public double getX() {
        return x;
    }

    /**
     * <p>
     * Sets the x coordinate of this Point.
     *
     * @param x the new x coordinate for this Point
     */
    public void setX(double x) {
        this.x = x;
    }

    /**
     * <p>
     * Gets the y coordinate of this Point.
     * </p>
     *
     * @return the y coordinate of this Point
     */
    public double getY() {
        return y;
    }

    /**
     * <p>
     * Sets the y coordinate of this Point.
     * </p>
     *
     * @param y the new y coordinate for this Point
     */
    public void setY(double y) {
        this.y = y;
    }
}