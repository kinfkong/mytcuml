package com.topcoder.diagraminterchange;

/**
 * <p>
 * A LeafElement is used when a model element has an optional representation. A LeafElement can be an Image, a
 * TextElement or a GraphicPrimitive and extends DiagramElement.
 * </p>
 * <p>
 * <b>Thread Safety:</b> This class is abstract but has no abstract methods. This class is thread-safe
 * because it has no attributes, but subclasses extending this one are not required to be thread-safe.
 * </p>
 *
 * @author caru, TCSDEVELOPER
 * @version 1.0
 */
public abstract class LeafElement extends DiagramElement {

    /**
     * <p>
     * Creates an instance of LeafElement.
     * </p>
     */
    protected LeafElement() {
        super();
    }
}