package com.topcoder.diagraminterchange;

/**
 * <p>
 * This class represents the Ellipse graphic primitive and extends GraphicPrimitive.
 * </p>
 * <p>
 * <b>Thread Safety:</b> This class is mutable and is not thread-safe, since thread safety is not required.
 * Instead, the calling application is required to ensure thread safety.
 * </p>
 *
 * @author caru, TCSDEVELOPER
 * @version 1.0
 */
public class Ellipse extends GraphicPrimitive {

    /**
     * <p>
     * Represents the center of this Ellipse. Can be null. Default to null.
     * </p>
     */
    private Point center;

    /**
     * <p>
     * Represents the x radius of this Ellipse. Default to 0.0.
     * </p>
     */
    private double radiusX;

    /**
     * <p>
     * Represents the y radius of this Ellipse. Default to 0.0.
     * </p>
     */
    private double radiusY;

    /**
     * <p>
     * Represents the rotation of this Ellipse. Default to 0.0.
     * </p>
     */
    private double rotation;

    /**
     * <p>
     * Represents the start angle of this Ellipse. Default to 0.0.
     * </p>
     */
    private double startAngle;

    /**
     * <p>
     * Represents the end angle of this Ellipse. Default to 0.0.
     * </p>
     */
    private double endAngle;

    /**
     * <p>
     * Creates an instance of Ellipse.
     * </p>
     */
    public Ellipse() {
        super();
    }

    /**
     * <p>
     * Gets the center of this Ellipse.
     * </p>
     *
     * @return the center of this Ellipse
     */
    public Point getCenter() {
        return center;
    }

    /**
     * <p>
     * Sets the center of this Ellipse. Can be null.
     * </p>
     *
     * @param center the new center of this Ellipse
     */
    public void setCenter(Point center) {
        this.center = center;
    }

    /**
     * <p>
     * Gets the x radius of this Ellipse.
     * </p>
     *
     * @return the x radius of this Ellipse
     */
    public double getRadiusX() {
        return radiusX;
    }

    /**
     * <p>
     * Sets the x radius of this Ellipse.
     * </p>
     *
     * @param radiusX the new x radius of this Ellipse
     */
    public void setRadiusX(double radiusX) {
        this.radiusX = radiusX;
    }

    /**
     * <p>
     * Gets the y radius of this Ellipse.
     * </p>
     *
     * @return the y radius of this Ellipse
     */
    public double getRadiusY() {
        return radiusY;
    }

    /**
     * <p>
     * Sets the y radius of this Ellipse.
     * </p>
     *
     * @param radiusY the new y radius of this Ellipse
     */
    public void setRadiusY(double radiusY) {
        this.radiusY = radiusY;
    }

    /**
     * <p>
     * Gets the rotation of this Ellipse.
     * </p>
     *
     * @return the rotation of this Ellipse
     */
    public double getRotation() {
        return rotation;
    }

    /**
     * <p>
     * Sets the rotation of this Ellipse.
     * </p>
     *
     * @param rotation the new rotation of this Ellipse
     */
    public void setRotation(double rotation) {
        this.rotation = rotation;
    }

    /**
     * <p>
     * Gets the start angle of this Ellipse.
     * </p>
     *
     * @return the start angle of this Ellipse
     */
    public double getStartAngle() {
        return startAngle;
    }

    /**
     * <p>
     * Sets the start angle of this Ellipse.
     * </p>
     *
     * @param startAngle the new start angle of this Ellipse
     */
    public void setStartAngle(double startAngle) {
        this.startAngle = startAngle;
    }

    /**
     * <p>
     * Gets the end angle of this Ellipse.
     * </p>
     *
     * @return the end angle of this Ellipse
     */
    public double getEndAngle() {
        return endAngle;
    }

    /**
     * <p>
     * Sets the end angle of this Ellipse.
     * </p>
     *
     * @param endAngle the new end angle of this Ellipse
     */
    public void setEndAngle(double endAngle) {
        this.endAngle = endAngle;
    }
}