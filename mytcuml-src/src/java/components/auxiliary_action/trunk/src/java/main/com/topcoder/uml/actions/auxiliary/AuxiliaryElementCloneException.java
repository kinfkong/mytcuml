package com.topcoder.uml.actions.auxiliary;

import com.topcoder.util.actionmanager.ActionExecutionException;

/**
 * <p>
 * This class extends the <code>ActionExecutionException</code> class,
 * and this exception is used if the the clone operation of auxiliary objects fails.
 * </p>
 * @version 1.0
 * @author Standlove, TCSDEVELOPER
 */
public class AuxiliaryElementCloneException extends ActionExecutionException {
    /**
     * <p>
     * Create a new <code>AuxiliaryElementCloneException</code> with error message.
     * </p>
     * @param message the error message.
     */
    public AuxiliaryElementCloneException(String message) {
        super(message);
    }

    /**
     * <p>
     * Create a new <code>AuxiliaryElementCloneException</code> with error message
     * and the cause exception.
     * </p>
     * @param message the error message
     * @param cause the inner cause.
     */
    public AuxiliaryElementCloneException(String message, Throwable cause) {
        super(message, cause);
    }
}
