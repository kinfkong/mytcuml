package com.topcoder.uml.actions.auxiliary;

import java.util.ArrayList;
import java.util.List;

import com.topcoder.uml.model.core.Namespace;
import com.topcoder.uml.standardclassloader.ClassDataLoaderConfigurationException;
import com.topcoder.uml.standardclassloader.StandardClassDataLoader;
import com.topcoder.uml.standardclassloader.UnknownLanguageException;

/**
 * <p>
 * Simple implementation of StandardClassDataLoader interface. Used for testing.
 * </p>
 *
 * @author TCSDEVELOPER
 * @version 1.0
 */
public class StandardClassDataLoaderImpl implements StandardClassDataLoader {

    /**
     * <p>
     * Constructs StandardClassDataLoader.
     * </p>
     */
    public StandardClassDataLoaderImpl() {
    }

    /**
     * <p>
     * Simple implementation of getNamespaces() method. Returns empty array.
     * </p>
     *
     * @param string string
     * @return list with namespaces
     * @throws UnknownLanguageException unknown language exception.
     * @throws ClassDataLoaderConfigurationException class data loader configuration exception.
     */
    public List<Namespace> getNamespaces(String string)
            throws UnknownLanguageException, ClassDataLoaderConfigurationException {
        return new ArrayList<Namespace>();
    }
}
