package com.topcoder.uml.actions.auxiliary.failuretests.impls;

import com.topcoder.uml.actions.auxiliary.AbstractAuxiliaryUndoableAction;
import com.topcoder.util.actionmanager.ActionExecutionException;

/**
 * <p>
 * Extends AbstractAuxiliaryUndoableAction. Used for testing.
 * </p>
 *
 * @author wiedzmin
 * @version 1.0
 */
public class AbstractAuxiliaryUndoableActionImpl extends AbstractAuxiliaryUndoableAction {

    /**
     * <p>
     * Constructs AbstractAuxiliaryUndoableAction.
     * </p>
     *
     * @param name the presentation name of the undoable action
     * @throws IllegalArgumentException if the argument is null or empty string.
     */
    public AbstractAuxiliaryUndoableActionImpl(String name) {
        super(name);
    }

    /**
     * <p>
     * Logs the error message and the exception.
     * </p>
     *
     * @param message the error message to log
     * @param exception the exception to log.
     * @throws IllegalArgumentException if the exception argument is null.
    */
    public void logException(String message, Throwable exception) {
        super.logException(message, exception);
    }

    /**
     * <p>
     * Redoes the action.
     * </p>
     *
     * @throws CannotRedoException if the action cannot be re-done
     */
    public void redo() {
        super.redo();
    }

    /**
     * <p>
     * Does nothing.
     * </p>
     *
     * @throws ActionExecutionException exception
     */
    public void execute() throws ActionExecutionException {
        //does nothing
    }
}
