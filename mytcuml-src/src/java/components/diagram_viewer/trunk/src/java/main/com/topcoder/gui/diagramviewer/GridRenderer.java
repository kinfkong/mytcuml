package com.topcoder.gui.diagramviewer;

import java.awt.Graphics;

/**
 * <p>
 * This interface defines the contract that every background renderer must
 * follow.
 * </p>
 * <p>
 * It contains only one method to render background grid for the DiagramView.
 * </p>
 * <p>
 * The implementations of this interface not required to be thread-safe. They
 * could be used in a thread safe manner in this component.
 * </p>
 * @author kakarotto, TCSDEVELOPER
 * @version 1.0
 */
public interface GridRenderer {
    /**
     * <p>
     * Render background grid for the given DiagramView.
     * </p>
     *
     * @param graphics the graphics context
     * @param diagramView the DiagramView for which the grid is rendered
     */
    public void renderGrid(Graphics graphics, DiagramView diagramView);
}
