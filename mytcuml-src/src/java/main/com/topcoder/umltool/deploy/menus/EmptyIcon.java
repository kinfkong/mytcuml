package com.topcoder.umltool.deploy.menus;

import java.awt.Component;
import java.awt.Graphics;

import javax.swing.Icon;

/**
 * <p>
 * Extended icon class for default use.
 * </p>
 * @author ly, FireIce, ylouis
 * @version 1.0
 */
public class EmptyIcon implements Icon {
    /**
     * <p>
     * Empty icon with size 16 by 16.
     * </p>
     */
    public static final Icon ICON_16 = new EmptyIcon(16, 16);

    /**
     * <p>
     * Empty icon with size 24 by 24.
     * </p>
     */
    public static final Icon ICON_24 = new EmptyIcon(24, 24);

    /**
     * <p>
     * Empty icon with size 32 by 32.
     * </p>
     */
    public static final Icon ICON_32 = new EmptyIcon(32, 32);

    /**
     * <p>
     * Represents the height of the icon.
     * </p>
     */
    private final int iconHeight;

    /**
     * <p>
     * Represents the width of the icon.
     * </p>
     */
    private final int iconWidth;

    /**
     * <p>
     * Creates an instance of <code>EmptyIcon</code>.
     * </p>
     * @param iconHeight
     *            height of the icon
     * @param iconWidth
     *            width of the icon
     */
    public EmptyIcon(int iconHeight, int iconWidth) {
        this.iconHeight = iconHeight;
        this.iconWidth = iconWidth;
    }

    /**
     * <p>
     * Gets the height of the icon.
     * </p>
     * @return the height of the icon
     */
    public int getIconHeight() {
        return iconHeight;
    }

    /**
     * <p>
     * Gets the height of the icon.
     * </p>
     * @return the weight of the icon
     */
    public int getIconWidth() {
        return iconWidth;
    }

    /**
     * Do nothing.
     * @param c
     *            component to paint
     * @param g
     *            graphics to paint
     * @param x
     *            x coordinate
     * @param y
     *            y coordinate
     */
    public void paintIcon(Component c, Graphics g, int x, int y) {
    }
}
