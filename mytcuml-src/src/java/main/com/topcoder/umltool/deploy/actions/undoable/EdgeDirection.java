package com.topcoder.umltool.deploy.actions.undoable;

/**
 * <p>
 * Represents the edge direction for sequence diagram.
 * </p>
 * @author ly, FireIce, ylouis
 * @version 1.0
 */
public enum EdgeDirection {
    /**
     * <p>
     * OUT direction represents the edge from the start lifeline to the end lifeline.
     * </p>
     */
    OUT,

    /**
     * <p>
     * IN direction represents the edge from the end lifeline back to the start lifeline.
     * </p>
     */
    IN
}
