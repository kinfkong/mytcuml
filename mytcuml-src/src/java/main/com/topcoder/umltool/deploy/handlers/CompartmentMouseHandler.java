package com.topcoder.umltool.deploy.handlers;

import java.awt.event.MouseEvent;

import javax.swing.event.MouseInputAdapter;

/**
 * <p>
 * Mouse handler for compartment of class node. It lets event pass behind.
 * </p>
 * @author ly
 * @version 1.0
 */
public class CompartmentMouseHandler extends MouseInputAdapter {

    /**
     * <p>
     * Handle <b>MOUSE_PRESSED</b> event.
     * </p>
     * <p>
     * Pass behind.
     * </p>
     * @param e
     *            the mouse pressed event
     */
    public void mousePressed(MouseEvent e) {
        if (e != null) {
            HandlerHelper.passBehind(e);
        }
    }

    /**
     * <p>
     * Handle the <b>MOUSE_DRAGGED</b> event.
     * </p>
     * <p>
     * Pass behind.
     * </p>
     * @param e
     *            the mouse dragged event
     */
    public void mouseDragged(MouseEvent e) {
        if (e != null) {
            HandlerHelper.passBehind(e);
        }
    }

    /**
     * <p>
     * Handle the <b>MOUSE_RELEASED</b> event.
     * </p>
     * <p>
     * Pass behind.
     * </p>
     * @param e
     *            the mouse released event
     */
    public void mouseReleased(MouseEvent e) {
        if (e != null) {
            HandlerHelper.passBehind(e);
        }
    }

    /**
     * <p>
     * Handle <b>MOUSE_ENTERED</b> event.
     * </p>
     * <p>
     * Pass behind.
     * </p>
     * @param e
     *            the mouse pressed event
     */
    public void mouseEntered(MouseEvent e) {
        if (e != null) {
            HandlerHelper.passBehind(e);
        }
    }

    /**
     * <p>
     * Handle <b>MOUSE_EXITED</b> event.
     * </p>
     * <p>
     * Pass behind.
     * </p>
     * @param e
     *            the mouse pressed event
     */
    public void mouseExited(MouseEvent e) {
        if (e != null) {
            HandlerHelper.passBehind(e);
        }
    }
}
