package com.topcoder.service.studio;
import java.lang.*;
/**
 * DTO class which is used to transfer contest data
*/
public class ContestData{
/**
 * content id
*/
private long contestId;
/**
 * content name
*/
private String name;
/**
 * project id
*/
private long projectId;
/**
 * TC Direct project id
*/
private long tcDirectProjectId;
/**
 * prizes for contest
*/
private List<PrizeData> prizes;
/**
 * date of contest launch
*/
private XMLGregorianCalendar launchDateAndTime;
/**
 * winner annoncement date
*/
private XMLGregorianCalendar winnderAnnoucementDeadline;
/**
 * duration hours
*/
private int durationInHours;
/**
 * contest category
*/
private long contestCategoryId;
/**
 * documents to upload
*/
private List<UploadedDocument> documentationUpload;
/**
 * contest payloads
*/
private List<ContestPayload> contestPayload;
/**
 * short summary of contest
*/
private String shortSummary;
/**
 * contest description
*/
private String contestDescriptionAndRequirements;
/**
 * required or restricted colors
*/
private String requiredOrRestrictedColors;
/**
 * required or restricted fonts
*/
private String requiredOrRestrictedFonts;
/**
 * size requirements
*/
private String sizeRequirements;
/**
 * other requirements and description
*/
private String otherRequirementsOrRestrictions;
/**
 * creator user
*/
private long creatorUserId;
/**
 * default file format, the same as in category
*/
private String finalFileFormat;
/**
 * list of all supported file types separated by commas
*/
private String otherFileFormats;
}

