package com.topcoder.service.studio;
import java.lang.*;
/**
 * DTO class which is used to transfer submission data
*/
public class SubmissionData{
/**
 * Id of submission
*/
private long submissionId;
/**
 * Id of submitter
*/
private long submitterId;
/**
 * contest id
*/
private long contestId;
/**
 * Submitted date
*/
private XMLGregorianCalendar submittedDateTimeStamp;
/**
 * URL of submission
*/
private String submissionContent;
/**
 * Flag if submission past screening
*/
private boolean passedScreening;
/**
 * Rank of submission
*/
private int placement;
/**
 * Check if submission being paid
*/
private boolean paidFor;
/**
 * Price of submission
*/
private double price;
/**
 * Check if submission marked for purchase
*/
private boolean markedForPurchase;
/**
 * check if submission is removed
*/
private boolean removed;
}

